def seed_client_accounts(client_id, conn=None, accounting_std="企业会计准则"):
    """Insert standard accounts for a new client based on accounting standard.
    Pass an existing open connection to avoid SQLite 'database is locked' errors.
    If no connection is given, a new one is opened and closed automatically.
    accounting_std: '企业会计准则' or '小企业会计制度'
    """
    own_conn = conn is None
    if own_conn:
        conn = get_db()
    c = conn.cursor()
    c.execute("SELECT COUNT(*) FROM accounts WHERE client_id=?", (client_id,))
    if c.fetchone()[0] > 0:
        if own_conn: conn.close()
        return
    acct_list = (STANDARD_ACCOUNTS if accounting_std == "企业会计准则"
                 else STANDARD_ACCOUNTS_SMALL)
    for code, name, full, typ, direction, parent, level in acct_list:
        c.execute(
            "INSERT OR IGNORE INTO accounts(client_id,code,name,full_name,type,direction,parent_code,level,is_default) VALUES(?,?,?,?,?,?,?,?,?)",
            (client_id, code, name, full, typ, direction, parent or None, level, 1)
        )
    if own_conn:
        conn.commit(); conn.close()
